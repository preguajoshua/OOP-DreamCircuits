    public void refreshTable(){
        //Load data
        if(SQLite.openDB()){
            String[] columns = {"repairID", "itemName","firstName","middleName","lastName","cost", "contactNo"};
            String[][] records = SQLite.read("tblRepair",columns);
            javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(records, columns){
                @Override
                public boolean isCellEditable(int row, int column) {
                   //all cells false
                   return false;
                }
            };
            this.tblRepair.setModel(model);
            
            //Additional COde Snippets for JTABLE behavior
            ListSelectionListener lsl = new ListSelectionListener(){
                public void valueChanged(javax.swing.event.ListSelectionEvent event) {
                    if (tblRepair.getSelectedRow() > -1) {
                        int row = tblRepair.getSelectedRow();
                        //JOptionPane.showMessageDialog(null, "Row is " + row);
                        if(SQLite.openDB()){
                            String sr_id = tblRepair.getValueAt(tblRepair.getSelectedRow(), 0).toString();
                            int id = Integer.parseInt(sr_id);
                            String[][] result = SQLite.read("tblRepair", "repairID=" + id);
                            txtID.setText(result[0][0]);
                            txtItemName.setText(result[0][1]);
                            txtFirstName.setText(result[0][2]);
                            txtMiddleName.setText(result[0][3]);
                            txtLastName.setText(result[0][4]);
                            txtCost.setText(result[0][5]);
                            txtContactNo.setText(result[0][6]);
                            SQLite.closeDB();
                        }
                    }
                }
            };
            this.tblRepair.getSelectionModel().addListSelectionListener(lsl);
            
            SQLite.closeDB();
        }        
    }


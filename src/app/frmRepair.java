    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if(SQLite.openDB()){
            try{
                int id = Integer.parseInt(txtID.getText());
                String itemName = this.txtItemName.getText();
                String firstName = this.txtFirstName.getText();
                String middleName = this.txtMiddleName.getText();
                String lastName = this.txtLastName.getText();
                String contactNo = this.txtContactNo.getText();
                String cost = this.txtContactNo.getText();
                String values  = "itemName="+ "'"+itemName +"'" +"," + "firstName="+"'"+firstName+"'"+","+"middleName="+"'"+middleName+"'"+","+"lastName="+"'"+lastName+"'"+","+"contactNo="+"'"+contactNo+"'"+","+"cost="+"'"+cost+"'";
            
                String sql = "UPDATE TABLE tblRepair SET("+values+") WHERE tblRepair="+id+"";
                System.out.println("QUERY IS " + sql);
                if(SQLite.updateRepair("tblRepair", values,id)){
                    JOptionPane.showMessageDialog(null, "User updated Successfully!");
                }
                else{
                    JOptionPane.showMessageDialog(null, "ERROR! User cannot be updated!");
                }
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Update Failed!");
            }

        }
        this.refreshTable();
        SQLite.closeDB();
    }//GEN-LAST:event_btnUpdateActionPerformed

